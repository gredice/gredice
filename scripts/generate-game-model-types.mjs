#!/usr/bin/env node

import { execFileSync, execSync } from 'node:child_process';
import { mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { platform } from 'node:os';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = resolve(__dirname, '..');
const assetsDir = join(repoRoot, 'assets');
const manifestPath = join(assetsDir, 'game-assets.json');
const gardenDir = join(repoRoot, 'apps', 'garden');
const tmpDir = join(gardenDir, '.tmp');
const tmpTypesGlb = join(tmpDir, 'GameAssetsTypes.glb');
const generatedModelsPath = join(
    repoRoot,
    'packages',
    'game',
    'src',
    'data',
    'gameAssetModels.generated.ts',
);

function getBlenderBinary() {
    if (process.env.BLENDER_BINARY) {
        return process.env.BLENDER_BINARY;
    }

    const os = platform();
    if (os === 'win32') {
        return 'C:\\Program Files\\Blender Foundation\\Blender 4.5\\blender.exe';
    }

    if (os === 'darwin') {
        return '/Applications/Blender.app/Contents/MacOS/Blender';
    }

    return 'blender';
}

function readManifest() {
    return JSON.parse(readFileSync(manifestPath, 'utf8'));
}

function getAssetModelUrl(asset) {
    const version =
        typeof asset.version === 'string' && asset.version.length > 0
            ? `?v=${asset.version}`
            : '';
    return `/assets/models/${asset.output}${version}`;
}

function writeGeneratedModels(manifest) {
    const assets = manifest.assets;
    const lines = [
        '// Auto-generated by scripts/generate-game-model-types.mjs.',
        '// Do not edit this file directly.',
        '',
        'export const gameAssetModels = {',
        ...assets.map(
            (asset) =>
                `    ${asset.name}: { url: ${JSON.stringify(getAssetModelUrl(asset))} },`,
        ),
        '} satisfies Record<string, { url: string }>;',
        '',
        'export type GameAssetName = keyof typeof gameAssetModels;',
        '',
    ];

    for (const preload of ['ground', 'primary', 'lazy']) {
        const names = assets
            .filter((asset) => asset.preload === preload)
            .map((asset) => `    '${asset.name}',`);

        lines.push(
            `export const ${preload}GameAssetNames = [`,
            ...names,
            '] satisfies GameAssetName[];',
            '',
        );
    }

    lines.push(
        'export const allGameAssetNames = [',
        ...assets.map((asset) => `    '${asset.name}',`),
        '] satisfies GameAssetName[];',
        '',
    );

    writeFileSync(generatedModelsPath, `${lines.join('\n')}\n`, 'utf8');
}

function exportTypesBundle() {
    mkdirSync(tmpDir, { recursive: true });
    execFileSync(
        getBlenderBinary(),
        [
            '-b',
            '--python',
            join(assetsDir, 'export-game-assets.py'),
            '--',
            '--skip-assets',
            '--types-output',
            tmpTypesGlb,
        ],
        {
            cwd: assetsDir,
            stdio: 'inherit',
        },
    );
}

function generateTypes() {
    execSync(
        `pnpm dlx gltfjsx@6.5.3 ${JSON.stringify(tmpTypesGlb)} --types --typeonly --output ../../packages/game/src/models/GameAssets.tsx`,
        {
            cwd: gardenDir,
            stdio: 'inherit',
        },
    );
    execFileSync('node', [join(repoRoot, 'scripts', 'fix-gltfjsx-output.mjs')], {
        cwd: repoRoot,
        stdio: 'inherit',
    });
}

try {
    const manifest = readManifest();
    writeGeneratedModels(manifest);
    exportTypesBundle();
    generateTypes();
} finally {
    rmSync(tmpTypesGlb, { force: true });
}
